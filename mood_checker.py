while True:
    mood = input("Please enter your mood: ")
    if mood == "happy":
        print("It is great to see you happy")
    elif mood == "nervous":
        print("Take a deep breath 3 times")
    elif mood == "sad":
        print("Always look on the bright side of death")
    elif mood == "excited":
        print("And I just can't hide it")
    elif mood == "relaxed":
        print("Happy chillin'!")
    else:
        print("I don't recognize this mood")